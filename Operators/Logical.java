public class Logical {
    public static void main(String args[]) {
        System.out.println((3 > 2) && (5 > 0)); // in && if both the statements are true then the output will be true.
        System.out.println((3 < 2) || (5 > 0)); // in || if any one statement is true then the output wil be true.
        System.out.println(!(3 < 2)); // ! changes true into false and vice versa.
    }
}
