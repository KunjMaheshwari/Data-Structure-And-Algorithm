public class Switch {
    public static void main(String args[]) {
        int number = 2;
        switch (number) {
            case 1:
                System.out.println("Hello World");
                break;
            case 2:
                System.out.println("VIT Bhopal");
                break;
            case 3:
                System.out.println("Kunj Maheshwari");
                break;
            default:
                System.out.println("Java Programming");
        }
    }
}
