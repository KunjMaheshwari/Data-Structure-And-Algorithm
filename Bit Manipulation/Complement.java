public class Complement {
    public static void main(String args[]) {
        System.out.println(~5); // ~ is the NOT Operator and the output will be ~6.
        System.out.println((5 << 2)); // << is the left shift operator and the output will be 20.
        System.out.println((6 >> 1)); // >> is the Right shift operator and the output will be 3.
    }
}