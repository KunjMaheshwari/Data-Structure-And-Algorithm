public class Basics{
  public static void main(String args[]){
    System.out.println((5 & 6));  // & is Binary AND Operator
    // Output of the above code will be 4.
    System.out.println((5|6)); // | is Binary OR Operator
    // Output of the above code will be 7.
    System.out.println((5^6)); // ^ is the Binary XOR Operator
    // Output of the above code will be 3. 
  }
}
